#include <stdio.h>

/**
 * main - function call for entry point
 *
 * Return: always 0
 *
 * int - data type for ordinary integers
 *
 * while - a loop operand
 *
 * putchar - function call for displaying characters on standard output
 */

int main(void)
{
	int t6;

	for (t6 = 48; t6 < 58; t6++)
	{
		putchar(t6);
	}
	putchar('\n');

	return (0);
}
