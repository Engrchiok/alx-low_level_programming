#include <stdio.h>

/**
 * main - function call for entry point
 *
 * Return: always 0
 *
 * int - data type for ordinary integers
 *
 * while - a loop operand
 *
 * printf - function call for displaying data on standard output
 */

int main(void)
{
	int t5 = 0;

	while (t5 < 10)
	{
		printf("%d", t5);
		t5++;
	}
	printf("\n");

	return (0);
}
